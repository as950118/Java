package ride;

public class Ride {
	private String rideNamekr;
	private String rideNameen;
	private String rideNumber;
	private String rideEmail;
	private String rideGender;
	private String rideCountry;
	private String rideModel;
	
	public String getRideModel() {
		return rideModel;
	}
	public void setRideModel(String rideModel) {
		this.rideModel = rideModel;
	}
	public String getRideGender() {
		return rideGender;
	}
	public void setRideGender(String rideGender) {
		this.rideGender = rideGender;
	}
	public String getRideNamekr() {
		return rideNamekr;
	}
	public void setRideNamekr(String rideNamekr) {
		this.rideNamekr = rideNamekr;
	}
	public String getRideNameen() {
		return rideNameen;
	}
	public void setRideNameen(String rideNameen) {
		this.rideNameen = rideNameen;
	}
	public String getRideNumber() {
		return rideNumber;
	}
	public void setRideNumber(String rideNumber) {
		this.rideNumber = rideNumber;
	}
	public String getRideEmail() {
		return rideEmail;
	}
	public void setRideEmail(String rideEmail) {
		this.rideEmail = rideEmail;
	}
	public String getRideCountry() {
		return rideCountry;
	}
	public void setRideCountry(String rideCountry) {
		this.rideCountry = rideCountry;
	}
}
